<h2>TO START USING THE NEW BUZZ <br />
FOLLOW THESE EASY STEPS</h2>
<h3>STEP 1</h3>
<p>To start the New Buzz, simply log onto<a href="http://www.minyanville.com"> Minyanville.com</a> and click the 'Buzz and Banter' link on the left nav. </p>
<h3>STEP 2</h3>
<p>After launching the New Buzz, please 
  enable Minyanville-only pop-ups in your 
browser for the optimal experience. </p>
<h4>MICROSOFT INTERNET EXPLORER USERS </h4>
<p>Follow these instructions:</p>
<ol>
  <li> In Internet Explorer, click Tools.</li>
  <li> Highlight Pop-up Blocker.</li>
  <li> Click on Pop-up Blocker Settings.</li>
  <li> In the Address of Web site to allow field type www.minyanville.com.</li>
  <li> Click Add, then click close.</li>
</ol>
<h4>MOZILLA FIREFOX USERS </h4>
<p>  Follow these instructions:</p>
<ol>
  <li> In Mozilla Firefox, click Tools.</li>
  <li> Next, click Options.</li>
  <li> Next to the Block Pop-up Windows 
      option, click Allowed Sites.</li>
  <li> In the Address of Web site field, 
      type www.minyanville.com.</li>
  <li> Click Allow, then click close.</li>
</ol>
<h2>TO UNINSTALL THE OLD APPLICATION, FOLLOW THESE EASY STEPS:</h2>
<ol>
  <li>    In Windows, click on the Start menu.</li>
  <li> Select Program Files and choose 
    Minyanville.</li>
  <li> Double click Uninstall and follow the 
    on-screen instructions.</li>
  <li> Delete any desktop shortcuts with the &quot;MV&quot; logo.</li>
</ol>
